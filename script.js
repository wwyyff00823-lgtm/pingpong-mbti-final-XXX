// ===================== 全局答题渲染、计分、开球网积分权重计算 =====================
let qIdx = 0;
let ansRecord = [];
let scoreObj = {I:0,E:0,S:0,N:0,T:0,F:0,J:0,P:0};

// 初始化测试
function initTest(){
  qIdx = 0;
  ansRecord = [];
  scoreObj = {I:0,E:0,S:0,N:0,T:0,F:0,J:0,P:0};
  renderQuestion();
  updateProgress();
  $("#btn-prev").disabled = true;
}

// 渲染单题
function renderQuestion(){
  const item = questionList[qIdx];
  $("#module-name").innerText = item.module;
  $("#question-title").innerText = item.title;
  $("#option-list").innerHTML = "";
  item.options.forEach((opt,idx)=>{
    const btn = document.createElement("button");
    btn.className = "w-full text-left px-4 py-3 border rounded-lg hover:bg-gray-50 transition";
    btn.innerText = `${idx+1}. ${opt.text}`;
    btn.onclick = ()=> pickAnswer(idx);
    $("#option-list").appendChild(btn);
  });
  $("#btn-prev").disabled = qIdx <= 0;
}

// 选择答案计分（叠加开球网积分权重）
function pickAnswer(optIdx){
  const item = questionList[qIdx];
  const selOpt = item.options[optIdx];
  ansRecord[qIdx] = selOpt.score;
  // 开球网积分权重：积分越高，战术维度权重系数微调
  let weight = 1.0;
  if(kaiqiuScore >= 1700) weight = 1.15;
  else if(kaiqiuScore >= 1500) weight = 1.08;
  else if(kaiqiuScore >= 1300) weight = 1.03;
  Object.keys(selOpt.score).forEach(k=>{
    scoreObj[k] += selOpt.score[k] * weight;
  });
}

// 更新进度条
function updateProgress(){
  const pct = ((qIdx+1)/totalQuestionNum*100).toFixed(1);
  $("#progress-bar").style.width = pct + "%";
  $("#progress-text").innerText = `${qIdx+1}/${totalQuestionNum}`;
}

// 上一题
$("#btn-prev").addEventListener("click",()=>{
  if(qIdx <= 0) return;
  qIdx--;
  renderQuestion();
  updateProgress();
});

// 下一题 / 结束计算结果
$("#btn-next").addEventListener("click",()=>{
  if(ansRecord[qIdx] === undefined){
    alert("请选择一个答案再继续！");
    return;
  }
  if(qIdx >= totalQuestionNum-1){
    calcResult();
    return;
  }
  qIdx++;
  renderQuestion();
  updateProgress();
});

// 人格判定计算
