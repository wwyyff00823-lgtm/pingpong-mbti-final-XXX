// 矛盾检测（Q5激进 + Q25保守 = 矛盾）
function calcIntegrity(answers) {
  let conflictCount = 0;
  let q5Tag = "", q25Tag = "";
  answers.forEach(item => {
    if (item.questionId === 5) q5Tag = item.tag;
    if (item.questionId === 25) q25Tag = item.tag;
  });
  if (q5Tag === "aggressive" && q25Tag === "conservative") conflictCount++;
  return { conflictCount, integrityScore: conflictCount < 3 };
}

// 计算MBTI类型
function calcMBTI(answers) {
  let score = {E:0,I:0,S:0,N:0,T:0,F:0,J:0,P:0};
  answers.forEach(item => {
    const s = item.score;
    for(let k in s) score[k] += s[k];
  });
  let mbti = "";
  mbti += score.E >= score.I ? "E" : "I";
  mbti += score.S >= score.N ? "S" : "N";
  mbti += score.T >= score.F ? "T" : "F";
  mbti += score.J >= score.P ? "J" : "P";
  return mbti;
}

// MD5 简易签名（后端复用，前端仅调用接口）
async function md5(str) {
  const encoder = new TextEncoder();
  const data = encoder.encode(str);
  const hashBuffer = await crypto.subtle.digest("MD5", data);
  const hashArray = Array.from(new Uint8Array(hashBuffer));
  return hashArray.map(b => b.toString(16).padStart(2, "0")).join("");
}

