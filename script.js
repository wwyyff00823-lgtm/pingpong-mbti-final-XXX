  const I_E = scoreObj.I >= scoreObj.E ? "I" : "E";
  const S_N = scoreObj.S >= scoreObj.N ? "S" : "N";
  const T_F = scoreObj.T >= scoreObj.F ? "T" : "F";
  const J_P = scoreObj.J >= scoreObj.P ? "J" : "P";
  const typeKey = I_E + S_N + T_F + J_P;
  const res = personalityMap[typeKey];

  // 赋值渲染结果卡片
