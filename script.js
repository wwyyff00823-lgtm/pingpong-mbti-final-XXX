// ====================== 下一题 & 提交答卷 ======================
nextBtn.addEventListener("click", () => {
    const checkedRadio = $("input[name='answer-radio']:checked");
    if (!checkedRadio) {
        alert("请选择一个你的应对选项");
        return;
    }
    const selectIdx = Number(checkedRadio.value);
    const currQ = questionPool[currentQuestionIndex];
    
    answerRecord.push({
        index: currentQuestionIndex,
        moduleId: currQ.moduleId,
        weight: currQ.weight,
        selectIdx: selectIdx,
        dimAssign: currQ.dimAssign
    });

    if (selectIdx <= 1) {
        dimensionWeighted[currQ.dimAssign[0]] += currQ.weight;
    } else {
        dimensionWeighted[currQ.dimAssign[1]] += currQ.weight;
    }

    if (currentQuestionIndex >= TOTAL_QUESTIONS - 1) {
        runConflictCheck();
        calcFinalPersonality();
        switchPage(pageResult);
        setTimeout(generateShareQRCode, 100);
        if (isReportUnlocked) {
            fullReportWrap.style.display = "block";
        }
        return;
    }
    currentQuestionIndex++;
    renderSingleQuestion();
});

// ====================== 交叉矛盾校验 + 置信度计算 ======================
function runConflictCheck() {
    const moduleGroups = {};
    answerRecord.forEach(item => {
        if (!moduleGroups[item.moduleId]) moduleGroups[item.moduleId] = [];
        moduleGroups[item.moduleId].push(item);
    });

    for (let mid in moduleGroups) {
        const items = moduleGroups[mid];
        const coreItems = items.filter(i => i.weight === 2.0);
        const subItems = items.filter(i => i.weight === 1.0);
        if (coreItems.length && subItems.length) {
            const coreAvg = coreItems.reduce((s,i)=>s+i.selectIdx,0) / coreItems.length;
            const subAvg = subItems.reduce((s,i)=>s+i.selectIdx,0) / subItems.length;
            if (Math.abs(coreAvg - subAvg) >= 2) {
                conflictCount++;
                const dimKey = coreItems[0].dimAssign[coreAvg <= 1 ? 0 : 1];
                dimensionWeighted[dimKey] *= 0.7;
            }
        }
    }

    if (conflictCount >= 3) {
        finalConfidence = 65;
        confidenceReason = "多道题目作答方向存在偏差，可能是答题时未认真选择，或自身打法尚未定型";
    } else if (conflictCount === 2) {
        finalConfidence = 78;
        confidenceReason = "部分题目作答存在小幅偏差，结果整体方向准确，细节可能略有出入";
    } else if (conflictCount === 1) {
        finalConfidence = 88;
        confidenceReason = "个别题目存在轻微偏差，整体匹配度较高，参考价值充足";
    } else {
        finalConfidence = 95;
        confidenceReason = "作答一致性很高，测试结果与你的真实球风匹配度极佳";
    }
}

// ====================== 计算最终人格并渲染结果 ======================
function calcFinalPersonality() {
    let code = "";
    code += dimensionWeighted.E >= dimensionWeighted.I ? "E" : "I";
    code += dimensionWeighted.S >= dimensionWeighted.N ? "S" : "N";
    code += dimensionWeighted.T >= dimensionWeighted.F ? "T" : "F";
    code += dimensionWeighted.J >= dimensionWeighted.P ? "J" : "P";

    const personKey = pingpongPersonMap[code] ? code : "ENTJ";
    const data = pingpongPersonMap[personKey];
    
    mbtiCodeText.innerText = data.code;
    funTitleText.innerText = data.funTitle;

    // 渲染人格图片，有地址就显示图片，没有就显示占位文字
    if (data.imgUrl && data.imgUrl.length > 0) {
        personImgBox.innerHTML = `<img src="${data.imgUrl}" alt="${data.funTitle}">`;
    } else {
        personImgBox.innerHTML = "人格示意图占位";
    }
    
    // 渲染三要点列表
    let descHtml = "";
    data.threeDesc.forEach(item => {
        descHtml += `<li>${item}</li>`;
    });
    descListBox.innerHTML = descHtml;
    
    fullReportContent.innerHTML = data.fullReport;

    // 置信度展示
    confidenceBlock.innerHTML = `
        <strong>本次测试准确度：${finalConfidence}%</strong><br>
        说明：${confidenceReason}
    `;
}

